#imprimindo apenas numeros com print
print(21,22,121)
print(21,22,121)

#O Modificador abaixo sep serve para colocar o que voce quiser entre os objetos
#Por padrão ele ja vem com sep=' ' ou seja um espaço em branco
print(21,22,121,sep="-")
print(21,22,121,sep='-')

#O modificador end abaixo serve para modificar o ultimo caractere do print
#Por padrão, ele vem com um \n. Tanto é que diferente do C, o print do python ja pula uma linha sozinho
print(21,22,121,end='eu')
print(21,22,121, end='\n')
print(21,22,121)

